'''
Moving onto Python Visualisation to learn how programming can be visualised

Python data visualization involves using a rich ecosystem of libraries to transform raw data into graphical formats like charts, graphs, 
and maps to help in analysis and decision-making.

Core Libraries
The Python ecosystem offers a diverse range of libraries, each suited for different needs: 
Matplotlib: The "foundation" library for static, animated, and interactive plots. It provides low-level control over every visual element but has a steeper learning curve.
Seaborn: Built on top of Matplotlib, it simplifies creating beautiful statistical graphics with better default styles and high-level functions.
Plotly: Specializes in high-quality interactive charts and web-based dashboards. It supports 3D charts, zooming, and tooltips.
Bokeh: Ideal for creating interactive, browser-ready visualizations and real-time streaming data applications without writing JavaScript.
Altair: A declarative library based on the "grammar of graphics" that allows users to create complex charts with minimal code

Common Plot Types
These libraries allow the creation of numerous chart types: 
Line Plots: Ideal for showing trends over time or continuous data analysis.
Bar Charts: Used for comparing different categories or discrete data.
Scatter Plots: Help in identifying relationships, correlations, and outliers between two or more variables.
Histograms: Visualize the distribution of a single variable to show frequency and data spread.
Box Plots (Box-and-Whisker): Effective for displaying the distribution, quartiles, and potential outliers in a dataset.
Heatmaps: Represent data in a matrix format using colors to show patterns or correlations, often used in statistical analysis. 
 
Why Use Python for Visualization?
Flexibility and Control: Python provides granular control over nearly every visual element of a plot, allowing for highly customized, publication-quality graphics.
Extensive Ecosystem: The vast collection of libraries and integration with data manipulation tools like Pandas and NumPy makes for a seamless data analysis workflow.
Integration: Visualizations can be easily integrated into various mediums, including Jupyter Notebooks, web applications, and business intelligence (BI) tools like Power BI.
Open Source and Community Support: The libraries are free to use, widely documented, and backed by large, active communities, providing ample resources for learning and troubleshooting. 
By transforming complex data into visuals, Python enables users to uncover patterns and trends that might otherwise remain hidden, ultimately leading to more informed, data-driven decisions. 
'''

